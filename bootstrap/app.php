            'Activity' => Spatie\Activitylog\Facades\ActivityLogger::class,
            'ensure.school.is.setup' => \App\Http\Middleware\EnsureSchoolIsSetup::class,
        ]);
    })
    ->withExceptions(function (Exceptions $exceptions) {
        // You can customize exception handling here if needed
    })
    ->create();

